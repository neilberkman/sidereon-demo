// Minimal, dependency-free syntax highlighter. A single master regex tokenizes
// comments, strings, numbers, and a small keyword set in one left-to-right pass,
// so a keyword inside a string or comment is never reparsed.
const KEYWORDS: Record<LangId, string[]> = {
  rust: ["use", "let", "fn", "pub", "vec", "mut", "None", "Some", "true", "false", "as"],
  python: ["import", "with", "as", "open", "print", "True", "False", "None", "for", "in"],
  c: ["include", "int64_t", "size_t", "double", "true", "false", "NULL", "struct", "return"],
  elixir: ["def", "do", "end", "true", "false", "nil"],
  js: ["import", "from", "const", "await", "new", "true", "false", "null", "let"],
};

function escapeHtml(s: string): string {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

export function highlight(code: string, lang: LangId): string {
  const comment =
    lang === "python" || lang === "elixir"
      ? "#[^\\n]*"
      : "\\/\\/[^\\n]*|\\/\\*[\\s\\S]*?\\*\\/";
  const kw = KEYWORDS[lang];
  const kwAlt = kw.length ? `\\b(?:${kw.join("|")})\\b` : "(?!x)x"; // never-match fallback

  const master = new RegExp(
    `(${comment})|("(?:[^"\\\\]|\\\\.)*")|(\\b(?:0x[0-9a-fA-F_]+|\\d[\\d_]*\\.?\\d*(?:[eE][+-]?\\d+)?)\\b)|(${kwAlt})`,
    "g",
  );

  return escapeHtml(code).replace(master, (m, com, str, num, key) => {
    if (com !== undefined) return `<span class="c-com">${m}</span>`;
    if (str !== undefined) return `<span class="c-str">${m}</span>`;
    if (num !== undefined) return `<span class="c-num">${m}</span>`;
    if (key !== undefined) return `<span class="c-kw">${m}</span>`;
    return m;
  });
}

// Sentinel lines wrapping a bulky embedded data literal (the SP3 ephemeris) so
// the renderer can fold it behind a collapsed toggle. They live on their own
// lines inside the snippet and are stripped before display and before copy, so
// they never reach the reader.
export const FOLD_OPEN = "SIDEREON_FOLD_OPEN";
export const FOLD_CLOSE = "SIDEREON_FOLD_CLOSE";

// Remove the fold sentinels, restoring the verbatim, copy-paste-ready snippet.
export function stripFold(code: string): string {
  return code.replace("\n" + FOLD_OPEN, "").replace("\n" + FOLD_CLOSE, "");
}

export interface FoldParts {
  head: string; // code before the data literal
  data: string; // the foldable data itself
  tail: string; // code after the data literal
}

// Split a snippet into the code before a foldable data literal, the data block
// itself, and the code after it. Returns null when the snippet has no fold.
export function splitFold(code: string): FoldParts | null {
  const re = new RegExp(`\\n${FOLD_OPEN}\\n([\\s\\S]*?)\\n${FOLD_CLOSE}\\n`);
  const m = code.match(re);
  if (!m || m.index === undefined) return null;
  return {
    head: code.slice(0, m.index),
    data: m[1],
    tail: code.slice(m.index + m[0].length),
  };
}
